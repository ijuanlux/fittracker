    // ============ PICKER ============

    fun pick(
        pool: List<String>,
        key: Int,
        sex: Sex,
        vararg replacements: Pair<String, String>,
    ): String {
        if (pool.isEmpty()) return ""
        val a = when (sex) {
            Sex.Male -> "o"
            Sex.Female -> "a"
            Sex.Unspecified -> "e"
        }
        val bro = when (sex) {
            Sex.Male -> "hermano"
            Sex.Female -> "hermana"
            Sex.Unspecified -> "marica"
        }
        val idx = ((key % pool.size) + pool.size) % pool.size
        var raw = pool[idx].replace(A_TOK, a).replace(BRO_TOK, bro)
        for ((k, v) in replacements) raw = raw.replace("{$k}", v)
        return raw
    }
